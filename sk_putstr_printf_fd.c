#include "ft_printf.h"

/* ************************************************************************** */
size_t	sk_putstr_printf_fd(char *s, int fd)
{
	if (fd == -1)
		return (0);
	if (!s)
	{
		write(fd, "(null)", 6);
		return (6);
	}
	write(fd, s, ft_strlen(s));
	return (ft_strlen(s));
}
